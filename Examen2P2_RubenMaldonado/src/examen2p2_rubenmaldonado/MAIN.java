    private void jButton30MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton30MouseClicked
        int vi = ((Number) vv.getValue()).intValue();
        String mi = mm.getText();
        Bateria b = null;
        Object o = null;
        for (int i = 0; i < bat.size(); i++) {
            String xy = ((Bateria) bat.get(i)).getMarca();
            if (xy.equals(mi)) {
                b = bat.get(i);
            }
        }
        for (int i = 0; i < com.size(); i++) {
            int xy = ((Combustion) com.get(i)).getVin();
            if (xy == vi) {
                o = com.get(i);
            }
        }
        for (int i = 0; i < hn.size(); i++) {
            int xy = ((HIbrido_N) hn.get(i)).getVin();
            if (xy == vi) {
                o = hn.get(i);
            }
        }
        for (int i = 0; i < he.size(); i++) {
            int xy = ((Hibrido_E) he.get(i)).getVin();
            if (xy == vi) {
                o = he.get(i);
            }
        }
        for (int i = 0; i < ele.size(); i++) {
            int xy = ((Electrico) ele.get(i)).getVin();
            if (xy == vi) {
                o = ele.get(i);
            }
        }

        if (o != null && b != null) {
            String y = b.getTipo();
            if (o instanceof Electrico && y.equals("electrico")) {
                try {
                    barra();
                } catch (InterruptedException ex) {
                    Logger.getLogger(MAIN.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else if (o instanceof HIbrido_N && y.equals("hibrido")) {
                try {
                    barra();
                } catch (InterruptedException ex) {
                    Logger.getLogger(MAIN.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else if (o instanceof Hibrido_E && y.equals("hibrido enchufable")) {
                try {
                    barra();
                } catch (InterruptedException ex) {
                    Logger.getLogger(MAIN.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(esamblaje, "No es posible realizar el esamblaje");
            }
        }


    }//GEN-LAST:event_jButton30MouseClicked


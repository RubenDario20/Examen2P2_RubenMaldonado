    private void jButton7MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton7MouseClicked

        if (table.getSelectedRow() >= 0) {
            DefaultTableModel Modelo
                    = (DefaultTableModel) table.getModel();
            Object marcaa = Modelo.getValueAt(table.getSelectedRow(), 0);
            Object modeloo = Modelo.getValueAt(table.getSelectedRow(), 1);
            String nombre = marcaa.toString();
            String mo = modeloo.toString();

            for (int i = 0; i < com.size(); i++) {
                String aux = ((Combustion) com.get(i)).getMarca();
                String aux2 = ((Combustion) com.get(i)).getModelo();
                if (aux.equals(nombre) && aux2.equals(mo)) {
                    auxiliar = i;
                    marca4.setText(((Combustion) com.get(i)).getMarca());
                    modelo4.setText(((Combustion) com.get(i)).getModelo());
                    vin4.setValue(((Combustion) com.get(i)).getVin());
                    cilindrada1.setValue(((Combustion) com.get(i)).getLitros());
                    cilindros1.setValue(((Combustion) com.get(i)).getCilindros());
                    L_K1.setText(((Combustion) com.get(i)).getConsumo());
                    String aux5 = ((Combustion) com.get(i)).getCarroceria();
                    if (aux5.equals("Familiar")) {
                        f4.setSelected(true);
                    } else if (aux5.equals("SUV")) {
                        s4.setSelected(true);
                    } else if (aux5.equals("Sub Compacto")) {
                        sc4.setSelected(true);
                    } else if (aux5.equals("Compacto")) {
                        c4.setSelected(true);
                    } else if (aux5.equals("Deportivo")) {
                        d4.setSelected(true);
                    }
                    modificarc.setModal(true);
                    modificarc.pack();
                    modificarc.setLocationRelativeTo(VerV);
                    modificarc.setVisible(true);
                    Modelo.removeRow(table.getSelectedRow());
                    table.setModel(Modelo);
                }
            }

        }


    }//GEN-LAST:event_jButton7MouseClicked


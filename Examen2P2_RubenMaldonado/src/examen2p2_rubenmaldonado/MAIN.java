    public void barra() throws InterruptedException {
        while (this.bar.getValue() != this.bar.getMaximum()) {
            this.bar.setValue(this.bar.getValue() + 1);
            Thread.sleep(200);
        }
        JOptionPane.showMessageDialog(esamblaje, "Esamblaje realizado exitosamente");
        this.bar.setValue(0);

        Thread.sleep(200);
    }

